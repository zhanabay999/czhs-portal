async function importNews() {
  const sql = neon(process.env.DATABASE_URL!);
  const db = drizzle(sql, { schema });

  console.log("Importing news articles from rail-news.kz...");

  // Get admin user to set as author
  const [admin] = await db
    .select()
    .from(schema.users)
    .where(eq(schema.users.employeeId, "001"))
    .limit(1);

  if (!admin) {
    console.error("Admin user not found! Run seed first.");
    return;
  }

  for (const article of articles) {
    try {
      await db.insert(schema.newsArticles).values({
        slug: article.slug,
        titleKk: article.titleKk,
        titleRu: article.titleRu,
        excerptKk: article.excerptKk,
        excerptRu: article.excerptRu,
        contentKk: article.contentKk,
        contentRu: article.contentRu,
        status: "published",
        isPinned: false,
        isInternal: false,
        publishedAt: article.publishedAt,
        authorId: admin.id,
      }).onConflictDoNothing();
      console.log(`  + ${article.titleRu}`);
    } catch (err) {
      console.error(`  x Error: ${article.slug}`, err);
    }
  }

  console.log("Import completed!");
}

